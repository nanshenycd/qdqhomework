package com.daily.homework.qdqhomework.how2j;

public class GirlFriends {
    /** 姓名**/
    String name;
    /** 身高**/
    int high;
    /** 提供**/
    int weight;
    /** 三围**/
    int threeLine;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHigh() {
        return high;
    }

    public void setHigh(int high) {
        this.high = high;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getThreeLine() {
        return threeLine;
    }

    public void setThreeLine(int threeLine) {
        this.threeLine = threeLine;
    }
}
